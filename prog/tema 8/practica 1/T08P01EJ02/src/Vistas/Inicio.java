    private void txtNombreActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtNombreActionPerformed
        switch(opciones){
           case 1 :
        txtDireccion.setEnabled(true);
        txtDireccion.requestFocus();
        txtNombre.setEnabled(false);
               break;
           case 2:
               
        Evento evento = Controlador.buscarevento(txtNombre.getText()) ;  
         if(!evento.equals(null))
        bCommit.setEnabled(true);
                break;
           case 3:  
         evento = Controlador.buscarevento(txtNombre.getText()); 
        if(!evento.equals(null)){
        txtDireccion.setEnabled(true);
        txtDireccion.setText(evento.getDireccion());
        txtFecha.setEnabled(true);
        txtFecha.setDate(evento.getFecha());
        txtHoraini.setEnabled(true);
        txtHoraini.setText(evento.getHorainicio().toString());
        txtHorafin.setEnabled(true);
        txtHorafin.setText(evento.getHorafin().toString());
        txtAforo.setEnabled(true);
        txtAforo.setText(Integer.toString(evento.getMaxpersona()));
        bCommit.setEnabled(true); }
                break;
                case 5 :
        evento = Controlador.buscarevento(txtNombre.getText()) ;  
         if(!evento.equals(null))
        bCommit.setEnabled(true);            
                break;
        }
    }//GEN-LAST:event_txtNombreActionPerformed

