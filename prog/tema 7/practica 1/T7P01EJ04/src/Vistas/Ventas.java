    private void txtProductoFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtProductoFocusLost
        if( !T7P01EJ04.Buscaproducto(txtProducto.getText())){
            txtProducto.setBackground(Color.red);
            errorprod.setVisible(true);
            txtImporte.setText("");
        }
        else{
            txtProducto.setBackground(Color.white);
            errorprod.setVisible(false);
        }
    }//GEN-LAST:event_txtProductoFocusLost

    private void txtUnidadesFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtUnidadesFocusLost

        if( !T7P01EJ04.Buscaexistencias(txtProducto.getText(),txtUnidades.getText())){
            txtUnidades.setBackground(Color.red);
            errorunid.setVisible(true);
            txtImporte.setText("");
        }
        else{
           txtUnidades.setBackground(Color.white);
           errorunid.setVisible(false);
        }
        buscaimporte();
    }//GEN-LAST:event_txtUnidadesFocusLost

