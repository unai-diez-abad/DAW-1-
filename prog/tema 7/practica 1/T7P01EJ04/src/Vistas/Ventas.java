    private void AceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AceptarActionPerformed
        if( !T7P01EJ04.Buscaproducto(txtProducto.getText())){
           JOptionPane.showMessageDialog(null,"No existe el producto selecionado" ); 
        }
        else{
        if( !T7P01EJ04.Buscaexistencias(txtProducto.getText(),txtUnidades.getText())){
           JOptionPane.showMessageDialog(null,"Se ha puesto mas unidades de las existentes" ); 
        }
        else
        {
            JOptionPane.showMessageDialog(null,"Venta realizada\n"+txtUnidades.getText()+" Unidades de "+ txtProducto.getText()+" Por "+txtImporte.getText()+"€"); 
            T7P01EJ04.vender(txtUnidades.getText(),txtUnidades.getText(),txtImporte.getText());
        }
        }
         T7P01EJ04.moverprincipio("venta");
    }//GEN-LAST:event_AceptarActionPerformed

